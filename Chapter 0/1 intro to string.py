a ='harry'       # Single quoted string
b = "harry"      # Double quoted string
c = '''harry'''  # Triple quoted string

# NOTE: String is immutable means after declaring it cannot be changed

# You can access particular character from the string using indexing. in python indexing starts from 0

print(a[2])
print(a[-1]) # this will print last character of the string a. Just like this you can also access second, third element etc by writing -2,-3,etc.

print(len(a)) # this is a way of printing length of any string using len() function