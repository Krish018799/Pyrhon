dno= int(input("Enter day no:"))

if dno == 1:
    print("Monday")
elif dno == 2:
    print("Tuesday")
elif dno == 3:
    print("Wednesday")
elif dno == 4:
    print("Thursday")
elif dno == 5:
    print("Friday")
elif dno == 6:
    print("Saturday")
elif dno == 7:
    print("Sunday")
else:
    print("Wrong option")